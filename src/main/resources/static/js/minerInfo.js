function ideaDecode() {
    var ciphertext = $('#idea_src').val();
    var seed = $('#idea_key').val();
    if (seed == "" || seed.length != 16) {
        alert("解密失败，请输入正确的密钥");
        return;
    }
    var url = [config.protocol, config.host, config.port, config.qtumUrl].join("");
    $.ajax({
        url: url,
        data: {ciphertext: ciphertext, seed: seed},
        type: "GET",
        dataType: 'JSON',
        success: function (data) {
            var status = data.body.status;
            if (status == "true") {
                $("#idea_dest").val(data.body.plaintext);
            } else {
                alert("解密失败，请输入正确的密文和密钥");
            }
        },
        error: function () {
            alert("解密失败，请输入正确的密文和密钥");
        }
    });
}